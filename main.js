// Обработчики для кнопок "Больше информации"
document.getElementById('more-info-btn-tu154').addEventListener('click', function() {
    var container = document.getElementById('info-container-tu154');
    if (container.style.display === 'none') {
        container.style.display = 'block';
    } else {
        container.style.display = 'none';
    }
});

